    public function blog() {
        /*phân trang các kết quả trả về cho ba trang mỗi trang và gán kết quả cho bài biến.*/
        $posts = Post::where('id', '>', 0)->paginate(3);
        /*đặt URL phân trang cho cơ sở URL + blog.*/
        $posts->setPath('blog');
        /*gán kết quả cho $ biến mảng dữ liệu.*/
        $data['posts'] = $posts;

        return view('blog', array('data' => $data, 'title' => 'Latest Blog Posts', 'description' => '', 'page' => 'blog', 'brands' => $this->brands, 'categories' => $this->categories, 'products' => $this->products));
    }

